//  ‚Ā†JavaScript Program to Check if a number is Positive, Negative, or Zero

function checkNumber(num) {
  if (num > 0) {
    console.log("positive number");
  } else if (num < 0) {
    console.log("negative number");
  } else if (num === 0) {
    console.log("zero");
  }
}

checkNumber(5);  
checkNumber(-2);  
checkNumber(0);   
